const _statsByLayer3 = new Map<string, Layer3Stat>(
  layer3StatsData.map(s => [s.layer3Category, s])
);

export const getLayer3Stat = (layer3: string): Layer3Stat | undefined =>
  _statsByLayer3.get(layer3);

export const calcTotalCapacity = (
  stat: Layer3Stat,
  platform: string
): number => {
  if (platform === 'sms') {
    return Math.round(stat.whiteUsers + stat.pinkUsers / 3);
  }
  return stat.whiteUsers + stat.pinkUsers + stat.blackUsers;
};

export const calcGradeCapacity = (
  stat: Layer3Stat,
  grade: AudienceGrade,
  platform: string
): number => {
  if (platform === 'sms') {
    if (grade === 'A') return Math.round(stat.bestWhite + stat.bestPink / 3);
    if (grade === 'B') return Math.round(stat.goodWhite + stat.goodPink / 3);
    return Math.round(stat.weakWhite + stat.weakPink / 3);
  }
  if (grade === 'A') return stat.bestWhite + stat.bestPink + stat.bestBlack;
  if (grade === 'B') return stat.goodWhite + stat.goodPink + stat.goodBlack;
  return stat.weakWhite + stat.weakPink + stat.weakBlack;
};
